CODE SEGMENT
	ASSUME CS:CODE ,DS:DATA
	
	PRINT PROC
		;ASSUMING CONTENT IN AL
		
		MOV DL,AL
		MOV AH,02H
		INT 21H
		RET
	PRINT ENDP
	
	INPUT PROC
		
		MOV AL,01H
		INT 21H
		MOV BL,AL
		
		MOV AL,01H
		INT 21H
		MOV BH,AL
		
		MOV AL,01H
		INT 21H
		MOV CL,AL
		
		MOV AL,01H
		INT 21H
		MOV CH,AL
		
		
		SUB BL,48
		SUB BH,48
		SUB CL,48
		SUB CH,48
		
		;BL*10+BH
		
		MOV AL,10
		MUL BL
		MOV BL,AL
		ADD BL,BH
		
		;CL*10+CH
		
		MOV AL,10
		MUL CL
		MOV CL,AL
		ADD CL,CH
		
		
		
		
		
		RET
	INPUT ENDP
	
	
	DISPLAY PROC
		
		MOV CX,9
		CMP CX,BX
		JC TWO
		
	ONE:	MOV AX,BX
		ADD AX,48
		CALL PRINT
		JMP EEND
		
	TWO:
		MOV CL,10 ;8 BIT NUMBER
		MOV AX,BX
		DIV CL
		
		
		MOV CH,AH
		SUB AH,AH
		MOV BX,AX
		
		PUSH CX
		CALL DISPLAY
		POP CX
		
		MOV AL,CH
		ADD AL,48
		CALL PRINT
		
		
	EEND:
		
		RET
	DISPLAY ENDP
	START:
		MOV AX,DATA
		MOV DS,AX
		
	;MAIN
	
	CALL INPUT
	
	MOV AL,BL
	MUL CL
	
	MOV BX,AX
	
	;NOW WE HAVE ANS IN BX
	
	CALL DISPLAY
	
		
	STOP:
		MOV AX,4C00H
		INT 21H
CODE ENDS
	END START
